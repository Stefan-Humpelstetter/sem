package com.napier.sem;

/**
 * Represents a city
 */
public class City {
    /**
     * City's id
     */
    public int id;

    /**
     * City's name
     */
    public String name;

    /**
     * City's country code
     */
    public String countryCode;

    /**
     * City's district
     */
    public String district;

    /**
     * City's population
     */
    public int population;
}
